    private void txtNameStudentKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNameStudentKeyReleased
        String PATTERN = "^[A-zA-Z\\s]*$"; // for a name
        Pattern patt = Pattern.compile(PATTERN);
        Matcher match = patt.matcher(txtNameStudent.getText());
        if (!match.matches()) {
            lblError.setText("Naming is incorrect!");
        } else {
            lblError.setText(null);
        }
    }//GEN-LAST:event_txtNameStudentKeyReleased

